#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n / 10 != 0)
		ft_putnbr_fd(n, fd);
	ft_putchar_fd(n % 10 + 48, fd);
}
